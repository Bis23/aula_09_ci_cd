def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(value1, value2):
    somar = value1 + value2
    return somar

def sub(value1, value2):
    subtrair = value1 - value2
    return subtrair

def mult(value1, value2):
    multiplicar = value1 * value2
    return multiplicar

def div(value1, value2):
    dividir = value1/value2
    return dividir